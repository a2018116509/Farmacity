     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $buscar= $request->buscar;
        if($buscar==''){
            $laboratorio = Laboratorio::all();
        }
        else{
            $laboratorio= Laboratorio::where('nombre', 'like', '%'.$buscar.'%')->get();
        }
        return $laboratorio;
    }

